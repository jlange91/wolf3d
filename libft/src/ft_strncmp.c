int	ft_strncmp(const char *s1, const char *s2, unsigned int n)
{
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	while (n > 0 && *str1 == *str2 && *str1 && *str2)
	{
		str1++;
		str2++;
		n--;
	}
	return ((n) ? (*str1 - *str2) : 0);
}
